public class AttributeExample02 {

    String fname = "John";
    String lname = "Doe";
    int age = 24;


    public static void main(String[] args) {
        AttributeExample02 myObj = new AttributeExample02();

        System.out.println("Name: " + myObj.fname + " " + myObj.lname ) ;
        System.out.println("Age : " + myObj.age);
     }
    
}
